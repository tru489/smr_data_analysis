close all;
addpath(genpath("..\..\helpers"));

path = "A:\thomasu\raw_data\2026-01-19 - Gas sensor stepping\stepping_0_21\20260119-1345_voltage.csv";
volt_tab = readtable(path);

time_min = (volt_tab.time_s - volt_tab.time_s(1)) / 60;

fh_volt_stepping = figure(Position=[2253         241        1230         725]);
scatter(time_min, volt_tab.voltage_gas_v)
ax=gca; ax.FontSize=12;
xlabel('Time (min)', FontSize=13)
ylabel('Sensor Voltage (V)', FontSize=13)
saveas(fh_volt_stepping, 'fig\voltage_stepping.jpg')

fh_df_voltage = figure;
scatter(time_min(1:end-1), diff(volt_tab.voltage_gas_v)/0.01/60)

time_ranges = [...
    0,0.75,...
    2.5, 4,...
    6,8.5,...
    10.8,11.6,...
    13.5,15.5,...
    17.5,18.5,...
    21,23,...
    25.25,26.25,...
    28.5,29.25,...
    31.5,34,...
    36,36.75,...
    38.5,40,...
    41.6,42.5,...
    45,46.25,...
    48,49,...
    50.2,51.2,...
    52.4,53.4,...
    54.6,55.6,...
    56.8,57.8,...
    59,59.6,...
    60.6,61.1,...
    62.5,64.25];
time_ranges = reshape(time_ranges, [2,length(time_ranges)/2])';

means = zeros(size(time_ranges, 1), 1);
avg_times = zeros(size(time_ranges, 1), 1);
for i = 1:size(time_ranges, 1)
    start_t = time_ranges(i,1); end_t = time_ranges(i,2); 
    mask = time_min > start_t & time_min < end_t;
    avg_times(i) = mean([start_t, end_t]);
    means(i) = mean(volt_tab.voltage_gas_v(mask));
end
pct0_cal = means(1);
pct100_cal = means(end);

fh_sat_stepping = figure(Position=[2253         241        1230         725]);
scatter(time_min, (volt_tab.voltage_gas_v - pct0_cal) / (pct100_cal - pct0_cal)*100)
ylim([0,100])
ax=gca; ax.FontSize=12;
xlabel('Time (min)', FontSize=13)
ylabel('O2 Air Saturation (%)', FontSize=13)
saveas(fh_sat_stepping, 'fig\sat_stepping.jpg')

fh_mean_voltages = figure;
means_rescale_pct = (means - pct0_cal) / (pct100_cal - pct0_cal)*100;
scatter(avg_times, means_rescale_pct)
ax=gca; ax.FontSize=12;
xlabel('Time (min)', FontSize=13)
ylabel('O2 Air Saturation (%)', FontSize=13)
saveas(fh_mean_voltages, 'fig\sat_stepping_avgs.jpg')